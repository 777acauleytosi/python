def main():
    print("Hello and welcome to the miles Calculator.")
    kilo = eval(input("What is the Kilometers measurement? "))
    mile = kilo * 0.62
    print("The measurement from kilometers to miles is in your case is", mile,"miles")
    input("Press the <Enter> key to quit.")
main()
    
