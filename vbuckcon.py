# A program to convert V-bucks to US dollars
# by Macauley Tosi

def main():
    print("Hello, Welcome to the V-buck calculator!!")
    print("This program converts US dollars into V-Bucks (Fortnite currency)")
    dollars = eval(input("How much money do you want to spend on V-bucks? "))
    vbucks = 100 * dollars
    print("The total amount of V-Bucks you have is", vbucks, "V-bucks.")
    print("Thank you for your credit card information.")
    input("press the <Enter> key to quit")
main()
